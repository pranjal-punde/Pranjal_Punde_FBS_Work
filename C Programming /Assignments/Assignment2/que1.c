void main()
{
	int ch='%';
	int a=10,b=5,c;
	if(ch=='+')
	{
		c=a+b;
		printf("%d",c);
	}
	if(ch=='-')
	{
		c=a-b;
		printf("%d",c);
	}
	if(ch=='*')
	{
		c=a*b;
		printf("%d",c);
	}
	if(ch=='/')
	{
		c=a/b;
		printf("%d",c);
	}
	if(ch=='%')
	{
		c=a%b;
		printf("%d",c);
	}
}